    import pickle
    from collections import Counter


    def weighted_mean(counter: Counter):
        total_weight = sum(counter.values())
        if total_weight == 0:
            return None
        return sum(float(k) * v for k, v in counter.items()) / total_weight


    def is_within_percent(counter_a: Counter, counter_b: Counter, threshold=0.3):
        # counter_a key가 float 변환 가능한지 확인
        try:
            [float(k) for k in counter_a.keys()]
        except ValueError:
            return False

        mean_a = weighted_mean(counter_a)
        mean_b = weighted_mean(counter_b)

        if mean_a is None or mean_b is None:
            return False

        # 상대 차이 계산
        relative_diff = abs(mean_a - mean_b) / max(abs(mean_a), abs(mean_b))

        return relative_diff <= threshold

